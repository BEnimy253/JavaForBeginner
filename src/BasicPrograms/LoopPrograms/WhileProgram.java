package BasicPrograms.LoopPrograms;

public class WhileProgram {
    public static void main(String[] args) {
        int count = 1;

        // In các số từ 1 đến 5 bằng vòng lặp while
        while (count <= 5) {
            System.out.println("Giá trị của count: " + count);
            count++;
        }
    }
}
